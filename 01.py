# Observe o trecho de código abaixo: int INDICE = 13, SOMA = 0, K = 0;  
INDICE = 13
SOMA = 0
K = 0
# Enquanto K for menor que INDICE, vamos somando
while K < INDICE:
    K = K + 1  # Aumenta o valor de K
    SOMA = SOMA + K  # Soma o valor de K em SOMA

print(SOMA)  # No final, imprime o valor total
# Ao final do processamento, qual será o valor da variável SOMA? 
# Retornou 91.